  <h2>Estrutura de repetição <i>while</i></h2>

  <?php

  $i = 1;
  while ($i < 11) {
    echo "<p>O valor de [i] = $i</p>";
    $i++;
  }

  ?>
    <!--
        Quando usar while

        O while deve ser usado quando o número de iterações não for conhecido ou quando a condição para terminar o loop não for conhecida.
